package com.example;

public class Person {
    private String fname;
    private String lname;

    public String getFname() {
        return this.fname;
    }

    public String getLname() {
        return this.lname;
    }

    public void setFname(String f) {
        this.fname = f;
    }

    public void setLname(String l) {
        this.lname = l;
    }
}
